let x = 200;

switch (x) {
    case x=100:
        console.log("x=100");
        break;
    case x=200:
        console.log("x=200");
        break;
    case x=300:
        console.log("x=300");
        break;
    case x=400:
        console.log("x=400");
        break;
    default:
        console.log("Enter Valid Number");
}

